    void loadDataTable() {
        model.setRowCount(0); // clear tabel dulu
        try (Connection conn = DriverManager.getConnection(koneksi, user, password);
                Statement stmt = conn.createStatement(); ResultSet rs = stmt.executeQuery
        ("SELECT * FROM member_gym ORDER BY id_member")) {

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("id_member"), // sesuaikan tipe data integer
                    rs.getString("nama"),
                    rs.getString("tipe_paket"),
                    rs.getString("coach")
                });
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error load data: " + e.getMessage());
        }
    }

