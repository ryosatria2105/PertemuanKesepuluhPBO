    private void btnUploadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUploadActionPerformed
      JFileChooser jfc = new JFileChooser(FileSystemView.getFileSystemView().getHomeDirectory());
    int returnValue = jfc.showOpenDialog(null);

    if (returnValue == JFileChooser.APPROVE_OPTION) {
        File filePilihan = jfc.getSelectedFile();

        try (BufferedReader br = new BufferedReader(new FileReader(filePilihan))) {
            Class.forName("org.postgresql.Driver");
            Connection conn = DriverManager.getConnection(
                "jdbc:postgresql://localhost:5432/DB_GYM",
                "postgres",
                "masryoo21"
            );

            String baris;
            String pemisah = ";"; // ubah menjadi "," jika CSV pakai koma
            boolean firstLine = true;

            while ((baris = br.readLine()) != null) {
                baris = baris.trim();
                if (baris.isEmpty()) continue; // skip baris kosong

                String[] data = baris.split(pemisah);
                if (data.length < 4) continue; // skip jika kolom kurang

                // skip header hanya baris pertama jika ada
                if (firstLine) {
                    firstLine = false;
                    if (data[0].toLowerCase().contains("id_member")) {
                        continue; 
                    }
                }

                try {
                    // hapus BOM jika ada
                    int idMember = Integer.parseInt(data[0].replace("\uFEFF","").trim());
                    String nama = data[1].trim();
                    String tipePaket = data[2].trim();
                    String coach = data[3].trim();

                    String sql = "INSERT INTO member_gym (id_member, nama, tipe_paket, coach) VALUES (?, ?, ?, ?)";
                    PreparedStatement pstmt = conn.prepareStatement(sql);
                    pstmt.setInt(1, idMember);
                    pstmt.setString(2, nama);
                    pstmt.setString(3, tipePaket);
                    pstmt.setString(4, coach);
                    pstmt.executeUpdate();
                    pstmt.close();
                } catch (NumberFormatException e) {
                    System.out.println("ID bukan angka: " + data[0]);
                } catch (SQLException e) {
                    System.out.println("Data gagal insert (mungkin duplikat): " + data[0]);
                }
            }

            conn.close();
            JOptionPane.showMessageDialog(null, "Upload CSV berhasil!");
            loadDataTable(); // refresh tabel di GUI

        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Gagal diinput: " + ex.getMessage());
        }
    }
    }//GEN-LAST:event_btnUploadActionPerformed

